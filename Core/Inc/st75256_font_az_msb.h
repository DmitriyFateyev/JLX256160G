static inline void draw_az_char(uint8_t *fb, uint16_t x, uint8_t page, char ch) {
  if (ch < 32 || ch > 126) ch = '?';
  const uint8_t *g = font_az[ch - 32];
  for (uint8_t col = 0; col < 7; col++) {
    fb[page * 256 + x + col]       = g[col];
    fb[(page + 1) * 256 + x + col]  = g[col + 7];
  }
}

static inline void draw_az_string(uint8_t *fb, uint16_t x, uint8_t page, const char *s) {
  while (*s) {
    draw_az_char(fb, x, page, *s++);
    x += 8;  /* 7px char + 1px gap */
  }
}

