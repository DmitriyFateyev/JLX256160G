void st75256_draw_hline(uint8_t *fb, int y) {
    if (y < 0 || y >= 160) return;
    int page = y / 8;
    int bit = y % 8;
    for (int x = 0; x < 256; x++) {
        fb[page * 256 + x] |= (1 << bit);
    }
}

