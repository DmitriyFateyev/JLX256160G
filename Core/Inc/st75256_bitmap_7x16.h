void st75256_draw_char_ru(uint8_t *fb, int x, int page, uint16_t unicode) {
    int index = -1;
    if (unicode >= 32 && unicode <= 126) {
        index = unicode - 32;
    } else if (unicode >= 0x0410 && unicode <= 0x044F) {
        index = unicode - 0x0410 + 95;
    }
    if (index < 0) return;
    
    const uint8_t *g = font_bmp[index];
    for (int col = 0; col < FONT_CHAR_W; col++) {
        if (x + col >= 256) break;
        fb[page * 256 + x + col] |= g[col];           // |= instead of =
        fb[(page + 1) * 256 + x + col] |= g[col + FONT_CHAR_W];  // |= instead of =
    }
}

void st75256_draw_string_ru(uint8_t *fb, int x, int page, const char *str, int advance) {
    const uint8_t *p = (const uint8_t *)str;
    while (*p) {
        uint16_t unicode = 0;
        if (*p < 0x80) {
            unicode = *p++;
        } else if ((*p & 0xE0) == 0xC0) {
            unicode = ((*p & 0x1F) << 6) | (*(p + 1) & 0x3F);
            p += 2;
        } else {
            p++;
            continue;
        }
        st75256_draw_char_ru(fb, x, page, unicode);
        x += advance;
    }
}

