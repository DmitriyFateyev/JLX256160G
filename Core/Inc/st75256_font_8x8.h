/**
 * @brief Draw a single 8x8 character at page-aligned position
 * @param fb Framebuffer pointer (5120 bytes)
 * @param x X coordinate (0-248)
 * @param page Page number (0-19), where page 0 = rows 0-7, page 1 = rows 8-15, etc.
 * @param c Character to draw (ASCII 32-126)
 */
void st75256_draw_char_8x8(uint8_t *fb, int x, int page, char c) {
    if (x < 0 || x > 248 || page < 0 || page > 19) return;
    if (c < 32 || c > 126) c = ' ';

    const uint8_t *glyph = font_8x8[c - 32];

    for (int col = 0; col < 8; col++) {
        fb[page * 256 + x + col] = glyph[col];
    }
}

/**
 * @brief Draw a string at page-aligned position
 * @param fb Framebuffer pointer
 * @param x Starting X coordinate
 * @param page Page number (0-19)
 * @param str Null-terminated string
 */
void st75256_draw_string_8x8(uint8_t *fb, int x, int page, const char *str) {
    while (*str && x <= 248) {
        st75256_draw_char_8x8(fb, x, page, *str++);
        x += 8;
    }
}

